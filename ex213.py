'''
Подвиг 4. На вход программе подается строка из слов, записанных через пробел. Необходимо прочитать эту строку,
разбить на слова на основе полученного списка составить прямоугольную таблицу из трех столбцов и N строк (число
строк столько, сколько получится). Лишнее (выходящее) слово - отбросить. Реализовать эту программу с использованием
функции zip. Результат отобразить на экране в виде прямоугольной таблицы из слов, записанных через пробел (в каждой
строчке).
Тесты: https://github.com/selfedu-rus/test-python-base/tree/main/9/9.5.4
Sample Input:
Москва Уфа Тула Самара Омск Воронеж Владивосток Лондон Калининград Севастополь
Sample Output:
Москва Уфа Тула
Самара Омск Воронеж
Владивосток Лондон Калининград
'''
input_string = 'Москва Уфа Тула Самара Омск Воронеж Владивосток Лондон Калининград Севастополь'
# Считывание строки из входного потока
#input_string = input().strip()
# Разделение строки на слова и отбрасывание лишнего слова
words = input_string.split()[:-1]
# Вычисление числа строк в таблице
num_rows = (len(words) + 2) // 3
# Создание трех столбцов таблицы с помощью функции zip
columns = [words[i::num_rows] for i in range(num_rows)]
# Вывод таблицы на экран в виде строк, разделенных пробелами
for row in zip(*columns):
    print(' '.join(row))
